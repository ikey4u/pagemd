    fn eps_sequence_with_arrows_stays_plain() {
        let html = render_html("（EPS：$8.71→$12.79→$15.88）");
        assert!(html.contains("<p>（EPS：$8.71→$12.79→$15.88）</p>"));
        assert_eq!(math_inline_count(&html), 0);
    }

