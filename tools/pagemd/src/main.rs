struct RenderedDocument {
    html: String,
    section_count: usize,
}

fn render_document(args: &CliArgs, title_hint: Option<&Path>) -> Result<RenderedDocument> {
    if args.inputs.is_empty() {
        bail!("Missing required input. Pass --input <FILE>.");
    }

    let ss = SyntaxSet::load_defaults_newlines();
    let ts = ThemeSet::load_defaults();

    let font_dir = find_katex_fonts(args.katex_fonts.as_deref())?;
    eprintln!("Using KaTeX fonts from: {}", font_dir);

    let mut sections: Vec<RenderedSection> = Vec::new();
    let mut doc_title = args.title.clone().unwrap_or_default();

    for input_path in &args.inputs {
        let base_dir = input_path
            .parent()
            .unwrap_or_else(|| Path::new("."))
            .to_path_buf();

        let source = std::fs::read_to_string(input_path)
            .with_context(|| format!("Cannot read {}", input_path.display()))?;

        let section = render_markdown(&source, &base_dir, args.math_font_size, &font_dir, &ss, &ts)
            .with_context(|| format!("Failed to render {}", input_path.display()))?;

        if doc_title.is_empty() && !section.title.is_empty() {
            doc_title = section.title.clone();
        }

        sections.push(section);
    }

    if doc_title.is_empty() {
        doc_title = title_hint
            .and_then(|p| p.file_stem())
            .and_then(|s| s.to_str())
            .or_else(|| args.inputs.first().and_then(|p| p.file_stem()).and_then(|s| s.to_str()))
            .unwrap_or("Document")
            .to_string();
    }

    let section_count = sections.len();
    let html = build_html(&doc_title, &sections);

    Ok(RenderedDocument { html, section_count })
}

fn write_document(args: &CliArgs, output: &Path, title_hint: Option<&Path>) -> Result<usize> {
    let document = render_document(args, title_hint)?;
    std::fs::write(output, document.html.as_bytes())
        .with_context(|| format!("Cannot write {}", output.display()))?;
    Ok(document.section_count)
}

fn sanitize_file_stem(value: &str) -> String {
    let stem = value
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() || c == '-' || c == '_' { c } else { '-' })
        .collect::<String>()
        .split('-')
        .filter(|part| !part.is_empty())
        .collect::<Vec<_>>()
        .join("-");

    if stem.is_empty() {
        "document".to_string()
    } else {
        stem
    }
}

fn default_view_output_path(args: &CliArgs) -> PathBuf {
    let stem = args
        .inputs
        .first()
        .and_then(|p| p.file_stem())
        .and_then(|s| s.to_str())
        .map(sanitize_file_stem)
        .unwrap_or_else(|| "document".to_string());
    let timestamp = SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|duration| duration.as_millis())
        .unwrap_or_default();

    std::env::temp_dir().join(format!("pagemd-{stem}-{timestamp}.html"))
}

fn open_in_browser(path: &Path) -> Result<()> {
    let path = path
        .canonicalize()
        .with_context(|| format!("Cannot resolve {}", path.display()))?;

    #[cfg(target_os = "macos")]
    let mut command = {
        let mut command = Command::new("open");
        command.arg(&path);
        command
    };

    #[cfg(target_os = "windows")]
    let mut command = {
        let mut command = Command::new("cmd");
        command.args(["/C", "start", ""]).arg(&path);
        command
    };

    #[cfg(all(unix, not(target_os = "macos")))]
    let mut command = {
        let mut command = Command::new("xdg-open");
        command.arg(&path);
        command
    };

    let status = command
        .status()
        .with_context(|| format!("Cannot open {}", path.display()))?;
    if !status.success() {
        bail!("Failed to open {}", path.display());
    }

    Ok(())
}

fn run_convert(args: &CliArgs) -> Result<()> {
    let output = args
        .output
        .as_deref()
        .context("Missing required output. Pass --output <FILE>.")?;
    let section_count = write_document(args, output, Some(output))?;

    eprintln!(
        "Written {} section(s) -> {}",
        section_count,
        output.display()
    );

    Ok(())
}

fn run_view(args: &CliArgs) -> Result<()> {
    let output = args.output.clone().unwrap_or_else(|| default_view_output_path(args));
    let title_hint = args.output.as_deref();
    let section_count = write_document(args, &output, title_hint)?;
    open_in_browser(&output)?;

    eprintln!(
        "Opened {} section(s) -> {}",
        section_count,
        output.display()
    );

    Ok(())
}

fn main() -> Result<()> {
    let cli = Cli::parse();

    match cli.command {
        Some(Commands::View(args)) => run_view(&args),
        None => run_convert(&cli.args),
    }
}

